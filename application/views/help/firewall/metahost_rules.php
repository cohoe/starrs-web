<em>Firewall Rules</em> come from two places, and are two main types: <em>Standalone</em> and <em>Metahost</em>.
<p>
<em>Metahosts</em> are groups of IP addresses that all share a common set of rules. Each member is updated with the rules of the metahost.
<p>
<em>Standalone</em> rules are assigned on a per-IP basis.
<p>
You can either specify a custom rule or choose from a list of common programs (for both Metahost and Standalone rules.
<p>
Each IP address has a <em>Default Firewall Action</em> that either denies or allows inbound traffic.
