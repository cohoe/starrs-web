Here you can configure all of the various DHCP options and settings in IMPULSE. There is a great deal of configuration that is available for the standard ISC-DHCPD.
<p>
<em>Classes</em> are groups of leases that can be configurated with certain options. 
<p>
<em>Global Options</em> govern all leases handed out unless otherwise overridden.
<p>
You can also <em>reload</em> the dhcpd.conf file in the database. Note that this does not reload it on the server, it is just for your benefit only.

