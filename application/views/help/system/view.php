Here you can view the details of your <em>system</em>. 
<p>
A system has an <em>Owner</em> who as RW access to the object and certain dependents. The <em>type</em> is used for general classification. The <em>Operating System</em> field is also used just to keep track of what is around. An optional <em>comment</em> can be specified. One year from your registration date, you will need to renew your system or it will be automatically deleted.
<p>
You can view all of the <em>Interfaces</em> attached on a system and <em>renew</em> the system.
<p>
You can also <em>Edit</em> or <em>Delete</em> a system if you are the owner.
