IP <em>ranges</em> are segments of consecutive IP addresses in a given subnet that allow for convienient management of resources.
<p>
The use code specifies what the address will be used for. 
<ul>
	<li>UREG = user registrations</li>
	<li>AREG = autoregistration</li>
	<li>RESV = reserved</li>
	<li>ROAM = roaming (dynamic)</li>
</ul>

A default DHCP class is then specified to allow for additional configuration. 
