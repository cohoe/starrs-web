IMPULSE is the replacement for the CSH Start registration system. Here's a general overview of how things work:
<p>
<em>Systems</em> are physical boxes that you want to connect to the network.
<em>Interfaces</em> are attached to systems. These are your NICs. 
<em>Addresses</em> are then assigned and configured on your NICs from available resources in your site
<em>DNS records</em> can be created on the addresses that are assigned.
<em>Firewall rules</em> can be applied to your addresses, as well as a default action (allow/deny). 
<p>
To get started, click <em>Quick Start</em> above or choose an item on the sidebar.
