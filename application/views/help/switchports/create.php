You can create a <em>switchport</em> on a <em>system</em> in two ways: 
<p>
A single port ("Gi0/1") by specifying the <em>Port Name</em>, the <em>Type</em>, and an optional <em>Description</em>
<p>
A range of ports can be created by specifying the <em>Prefix</em> (Gi0/), then the first and last port numbers.
