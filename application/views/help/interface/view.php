Here you can view the details of a specific <em>interface</em>. The <em>name</em> is a logical identifier specific to your system (eth0, Local Area Connection, etc). The <em>MAC</em> address identifies the <em>interface</em> on the network and is globally unique. An optional <em>comment</em> can be specified. 
<p>
You can view <em>addresses</em> on the <em>interface</em> or <em>edit</em>/<em>delete</em> the interface.

